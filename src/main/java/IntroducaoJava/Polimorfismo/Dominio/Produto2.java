package IntroducaoJava.Polimorfismo.Dominio;

public  class Produto2 {
    private String nome;
    private double preco;

    public Produto2(String nome, double preco) {
        this.nome = nome;
        this.preco = preco;
    }

    public String getNome() {
        return this.nome;
    }
    public double getPreco() {
        return this.preco;
    }

}
